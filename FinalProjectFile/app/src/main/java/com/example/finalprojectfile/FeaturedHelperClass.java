package com.example.finalprojectfile;

public class FeaturedHelperClass {

    int image;
    String statenames;

    public FeaturedHelperClass(int image, String statenames){
        this.image = image;
        this.statenames = statenames;
    }

    public int getImage() {
        return image;
    }

    public String getStatenames() {
        return statenames;
    }
}
